import UIKit

extension UIFont {
  
  static var avenirNextMedium30: UIFont {
    return UIFont(name: "Avenir Next Medium", size: 30) ?? UIFont.systemFont(ofSize: 30)
  }
  
  static var avenirNextMedium25: UIFont {
    return UIFont(name: "Avenir Next Medium", size: 25) ?? UIFont.systemFont(ofSize: 25)
  }
  
  static var avenirNextMedium20: UIFont {
    return UIFont(name: "Avenir Next Medium", size: 20) ?? UIFont.systemFont(ofSize: 20)
  }
  
  static var avenirNextMedium18: UIFont {
    return UIFont(name: "Avenir Next Medium", size: 18) ?? UIFont.systemFont(ofSize: 18)
  }
  
  static var avenirNextMedium17: UIFont {
    return UIFont(name: "Avenir Next Medium", size: 17) ?? UIFont.systemFont(ofSize: 17)
  }
  
  static var avenirNextMedium10: UIFont {
    return UIFont(name: "Avenir Next Medium", size: 10) ?? UIFont.systemFont(ofSize: 10)
  }
  
}
